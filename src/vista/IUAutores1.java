    private int[] getIdsAutores(LinkedList<Autor> listaAutores) {
        int [] arrIA=new int[listaAutores.size()];
        for (int i = 0; i < listaAutores.size(); i++) {
           arrIA[i] = listaAutores.get(i).getIdautores();
        }
        return arrIA;
    }
